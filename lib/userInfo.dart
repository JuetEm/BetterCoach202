class UserInfo {
  const UserInfo(
    this.docId,
    this.uid,
    this.name,
    this.registerDate,
    this.phoneNumber,
    this.registerType,
    this.goal,
    this.info,
    this.note,
    this.isActive,
  );
  final String docId;
  final String uid;
  final String name;
  final String registerDate;
  final String phoneNumber;
  final String registerType;
  final String goal;
  final String info;
  final String note;
  final bool isActive;
}
